package com.realmattersid.model;

public class DataNews {
    private int id;
    private String  title_news, image_news, url_news;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle_news() {
        return title_news;
    }

    public void setTitle_news(String title_news) {
        this.title_news = title_news;
    }

    public String getImage_news() {
        return image_news;
    }

    public void setImage_news(String image_news) {
        this.image_news = image_news;
    }

    public String getUrl_news() {
        return url_news;
    }

    public void setUrl_news(String url_news) {
        this.url_news = url_news;
    }
}
