package com.realmattersid.model;

public class DataClinic {
    private int id;
    private String  name_clinic, address_clinic, phone_clinic, website_clinic;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName_clinic() {
        return name_clinic;
    }

    public void setName_clinic(String name_clinic) {
        this.name_clinic = name_clinic;
    }

    public String getAddress_clinic() {
        return address_clinic;
    }

    public void setAddress_clinic(String address_clinic) {
        this.address_clinic = address_clinic;
    }

    public String getPhone_clinic() {
        return phone_clinic;
    }

    public void setPhone_clinic(String phone_clinic) {
        this.phone_clinic = phone_clinic;
    }

    public String getWebsite_clinic() {
        return website_clinic;
    }

    public void setWebsite_clinic(String website_clinic) {
        this.website_clinic = website_clinic;
    }
}
